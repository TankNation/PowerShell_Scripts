Write-Host "`nLets find that MSI Product Code for you" -ForegroundColor Green

Do{
    $Software = Read-Host -Prompt "`nWhat is the application name (or part of it)?" 

    Get-WmiObject Win32_Product -filter "Name Like '%$Software%'" | Sort-Object Name | Format-Table Name, Version, Vendor, IdentifyingNumber
        
    $runAgain = Read-Host -Prompt "Run Again (Y/N)?"

}While ($runAgain -eq 'Y')

